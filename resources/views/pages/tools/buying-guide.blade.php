            <section id="step1" class="mb-12 scroll-mt-24">
                <h2>1. Understanding the Market</h2>
                <p>
                    The UK community pharmacy market consists of approximately 11,000 pharmacies, with a mix of 
                    independents, small groups, and large multiples. Before starting your search, it's essential 
                    to understand the current landscape.
                </p>
                <h3>Key considerations:</h3>
                <ul>
                    <li><strong>NHS funding:</strong> The majority of pharmacy income comes from NHS dispensing fees and services</li>
                    <li><strong>PCN involvement:</strong> Primary Care Network partnerships are increasingly important</li>
                    <li><strong>Market trends:</strong> Items per pharmacy, service mix, and retail competition</li>
                    <li><strong>Regional variations:</strong> Significant differences in pricing between regions</li>
                </ul>
            </section>

            <section id="step2" class="mb-12 scroll-mt-24">
                <h2>2. Financing Your Purchase</h2>
                <p>
                    Most pharmacy purchases are financed through a combination of bank lending and personal equity. 
                    Understanding your financing options early is crucial.
                </p>
                <h3>Typical funding structure:</h3>
                <ul>
                    <li><strong>Bank debt:</strong> 50-70% of purchase price (specialist pharmacy lenders)</li>
                    <li><strong>Personal equity:</strong> 20-35% deposit typically required</li>
                    <li><strong>Stock finance:</strong> Often arranged separately from the main loan</li>
                </ul>
                <div class="bg-green-50 border border-green-200 rounded-lg p-4 my-6">
                    <h4 class="text-green-800 font-semibold mb-2">Specialist Pharmacy Lenders</h4>
                    <p class="text-green-700 text-sm mb-0">
                        Major banks with pharmacy lending teams include Lloyds, NatWest, Barclays, and HSBC. 
                        Alternative lenders like Christie Finance and specialist brokers can also help.
                    </p>
                </div>
            </section>

            <section id="step3" class="mb-12 scroll-mt-24">
                <h2>3. Finding the Right Pharmacy</h2>
                <p>
                    Once you understand your budget and financing, it's time to search for suitable opportunities.
                </p>
                <h3>Where to look:</h3>
                <ul>
                    <li>Specialist pharmacy sales platforms (like P3 Pharmacy!)</li>
                    <li>Pharmacy brokers and agents</li>
                    <li>Direct approaches to pharmacy owners</li>
                    <li>Professional networks and word of mouth</li>
                </ul>
                <h3>Key criteria to consider:</h3>
                <ul>
                    <li>Monthly NHS items and trends</li>
                    <li>Gross profit and margins</li>
                    <li>Location and local demographics</li>
                    <li>Competition and nearby prescribers</li>
                    <li>Property tenure (freehold vs leasehold)</li>
                    <li>Staff and their contracts</li>
                </ul>
            </section>

            <section id="step4" class="mb-12 scroll-mt-24">
                <h2>4. Due Diligence</h2>
                <p>
                    Thorough due diligence is essential before committing to a purchase. This typically takes 4-8 weeks.
                </p>
                <h3>Financial due diligence:</h3>
                <ul>
                    <li>3 years of accounts and tax returns</li>
                    <li>NHS payment schedules and FP34s</li>
                    <li>Stock valuation methodology</li>
                    <li>Creditor and debtor analysis</li>
                </ul>
                <h3>Operational due diligence:</h3>
                <ul>
                    <li>Staff contracts and TUPE considerations</li>
                    <li>NHS contract verification</li>
                    <li>Equipment and fixtures assessment</li>
                    <li>Supplier agreements and rebates</li>
                </ul>
                <h3>Legal and property:</h3>
                <ul>
                    <li>Lease review (if leasehold)</li>
                    <li>Planning and building compliance</li>
                    <li>NHS England registration requirements</li>
                    <li>GPhC premises registration</li>
                </ul>
            </section>

            <section id="step5" class="mb-12 scroll-mt-24">
                <h2>5. Making an Offer</h2>
                <p>
                    Offers are typically made "subject to contract" and subject to satisfactory due diligence.
                </p>
                <h3>Offer structure:</h3>
                <ul>
                    <li><strong>Goodwill:</strong> The main value of the business</li>
                    <li><strong>Stock:</strong> Usually at cost, valued separately at completion</li>
                    <li><strong>Fixtures & fittings:</strong> Often included in goodwill</li>
                    <li><strong>Property:</strong> If freehold is included</li>
                </ul>
                <div class="bg-amber-50 border border-amber-200 rounded-lg p-4 my-6">
                    <h4 class="text-amber-800 font-semibold mb-2">Negotiation Tips</h4>
                    <p class="text-amber-700 text-sm mb-0">
                        Be prepared to negotiate but remain realistic. Understand what drives value in the 
                        specific pharmacy and focus negotiations on areas of genuine concern.
                    </p>
                </div>
            </section>

            <section id="step6" class="mb-12 scroll-mt-24">
                <h2>6. Legal Process</h2>
                <p>
                    Once heads of terms are agreed, solicitors are instructed to handle the legal completion.
                </p>
                <h3>Key legal steps:</h3>
                <ul>
                    <li>Drafting and negotiating the sale agreement</li>
                    <li>NHS change of ownership application</li>
                    <li>Lease assignment (if applicable)</li>
                    <li>TUPE consultation with staff</li>
                    <li>Bank facility documentation</li>
                </ul>
                <h3>Typical timeline:</h3>
                <p>
                    The legal process typically takes 8-12 weeks from instruction to completion, though 
                    NHS approval can sometimes extend this.
                </p>
            </section>

            <section id="step7" class="mb-12 scroll-mt-24">
                <h2>7. Completion & Handover</h2>
                <p>
                    On completion day, ownership transfers and you take control of the pharmacy.
                </p>
                <h3>Completion checklist:</h3>
                <ul>
                    <li>Final stock count and payment</li>
                    <li>Keys and access handover</li>
                    <li>Staff introductions and communication</li>
                    <li>Supplier account transfers</li>
                    <li>Insurance and utilities transfer</li>
                    <li>PMR system access and training</li>
                </ul>
                <h3>First 100 days:</h3>
                <p>
                    The first few months are critical. Focus on learning the business, building relationships 
                    with staff and customers, and implementing any immediate improvements while maintaining 
                    continuity of service.
                </p>
            </section>
